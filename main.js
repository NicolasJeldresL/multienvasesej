function abrirDesc(evt, abrirDescripciones) {
    var i, tabcontent, tablinks;
    tabcontent = document.getElementsByClassName("content-info");
    for (i = 0; i < tabcontent.length; i++) {
      tabcontent[i].style.display = "none";
    }
    tablinks = document.getElementsByClassName("desplinks");
    for (i = 0; i < tablinks.length; i++) {
      tablinks[i].className = tablinks[i].className.replace(" active", "");
    }
    document.getElementById(abrirDescripciones).style.display = "block";
    evt.currentTarget.className += " active";
  }

